package day03;

public class expressions {
    public static void main(String[] args){
        // expresions = operands & operators
        // operands = values, variables, number, quantity
        // operators = + - / %

        // addition
        int friends = 10;
        friends = friends + 1;
        System.out.println(friends);

        // subtration

        int friends2 = 10;
        friends2 = friends2-1;
        System.out.println(friends2);

        // multiplication

        int friends3 = 5;
        friends3 = friends3 * 2;
        System.out.println(friends3);

        // division

        double friends4 = 10;
        friends4 = (double)friends4/3;
        System.out.println(friends4);

        // modulas

        int friends5 = 10;
        friends5 = friends5 % 3;
        System.out.println(friends5);

    }
}
